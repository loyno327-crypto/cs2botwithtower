# ---------- Уровневые плюшки: кейсы и скидки ----------
# Каждый кейс в static/cases.json теперь может иметь поля:
#   "min_level" — с какого уровня кейс становится доступен для открытия
#                 (если поля нет — кейс доступен всем, с 1 уровня);
#   "xp"        — сколько опыта даёт открытие именно этого кейса
#                 (если поля нет — используется XP_CASE_OPEN как раньше).
# Так более "дорогие"/редкие кейсы дают больше опыта, чем стартовые.

# Скидка на ПОКУПКУ (открытие) любого кейса растёт вместе с уровнем игрока:
# за каждые LEVEL_DISCOUNT_STEP уровней скидка увеличивается на
# LEVEL_DISCOUNT_PER_STEP процентов, но не выше LEVEL_DISCOUNT_MAX.
# Пример при значениях по умолчанию: 5 уровень -3%, 10 уровень -6%, ...,
# 50+ уровень -30% (упирается в потолок).
LEVEL_DISCOUNT_STEP = 5
LEVEL_DISCOUNT_PER_STEP = 3
LEVEL_DISCOUNT_MAX = 30


def level_discount_percent(level: int) -> int:
    """Возвращает текущую скидку на кейсы в процентах для указанного уровня."""
    discount = (level // LEVEL_DISCOUNT_STEP) * LEVEL_DISCOUNT_PER_STEP
    return min(discount, LEVEL_DISCOUNT_MAX)

